                {
                    aux.y += 1;
                    Instantiate(normalfloor, aux, Quaternion.identity);
                }
                else if (map[i, j] == 4)
                {
                    aux.y += 0.001f;
                    Instantiate(spawn, aux, Quaternion.identity);
                }
                color = !color;
            }
        }
    }

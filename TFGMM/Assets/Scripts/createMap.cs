            for (int j = 0; j < map.GetLength(1); j++)
            {
                Vector3 aux = startPos;
                aux.x += j;
                aux.z += i;

                if(map[i, j] == 1 || map[i, j] == 3 || map[i, j] == 4)
                {
                    if (color)
                    {
                        Instantiate(normalfloor, aux, Quaternion.identity);
                    }
                    else
                    {
                        Instantiate(darkfloor, aux, Quaternion.identity);
                    }
                }
                else if(map[i, j] == 2)
                {
                    Instantiate(water, aux, Quaternion.identity);
                }

